#include <bits/stdc++.h>
using namespace std;
int main(){
	cout<<"check";
	int x=3;
	int y;
	y=x++;
	cout<<endl<<"x is"<<x<<endl<<"y is"<<y;
	y=++x;
	cout<<endl<<"x is"<<x<<endl<<"y is"<<y;
	y=x--;
	cout<<endl<<"x is"<<x<<endl<<"y is"<<y;
	y=--x;
	cout<<endl<<"x is"<<x<<endl<<"y is"<<y;
	return 0;
}
